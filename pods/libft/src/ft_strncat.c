#include "libft.h"

char			*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	y;

	i = 0;
	y = 0;
	while (s1[i])
		++i;
	while (s2[y] && y < n)
	{
		s1[i] = s2[y];
		++i;
		++y;
	}
	s1[i] = 0;
	return (s1);
}
